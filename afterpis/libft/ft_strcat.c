#include "libft.h"

char	*strcat(char *s1, const char *s2)
{
	int i;

	i = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[i] != '\0')
	{
		s1[i] = s2[i];
		i++;
	}
	s1[i] = '\0';
}
