#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char c_c;

	c_c = (char)c;
	while (*s != c_c && *s != '\0')
		s++;
	if (*s == '\0' && c_c != '\0')
		return (NULL);
	return ((char*)s);
}
